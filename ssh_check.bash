#!/bin/bash
# author: YiLing Wu (hj)
# date: 2023-12-23 15:21
# description: 此脚本用于通过 SSH 批量在多台服务器上执行命令。
#              它会遍历一组目标服务器的 IP 地址，使用 root 用户登录，
#              并在每台服务器上执行传递给脚本的命令。
#              适用于自动化任务，例如检查服务器状态、更新配置或管理服务。
# 用法: ./ssh_check.sh "ls /home"   # 在所有服务器上列出 /home 目录

# 目标服务器的 IP 列表
ip_list="192.168.10.7 192.168.10.31 192.168.10.41 192.168.10.51"

# 输出批量操作的提示信息
echo '--------------------------------------------'
echo '批量执行命令'
echo '--------------------------------------------'

# 遍历每个 IP 并执行传入的命令
for ip in $ip_list
do
  ssh root@$ip $@
done
